#include "init_uart.h"
#include "stm32f4xx_hal.h"
#include <string.h>


UART_HandleTypeDef huart2;


/*!	\brief Initializes the UART peripheral for communicating with the host
*/
void initUart(uint32_t baudrate)
{
	__HAL_RCC_USART2_CLK_ENABLE();

	memset(&huart2, 0, sizeof(UART_HandleTypeDef));

	huart2.Lock = HAL_UNLOCKED;
	huart2.Instance = USART2;
	huart2.Init.BaudRate = baudrate;
	huart2.Init.WordLength = UART_WORDLENGTH_8B;
	huart2.Init.StopBits = UART_STOPBITS_1;
	huart2.Init.Parity = UART_PARITY_NONE;
	huart2.Init.Mode = UART_MODE_TX_RX;
	huart2.Init.HwFlowCtl = UART_HWCONTROL_NONE;
	huart2.Init.OverSampling = UART_OVERSAMPLING_16;

	if (HAL_UART_Init(&huart2) != HAL_OK)
	{
		rterror("HAL_UART_Init failed");
	}
}

/*!	\brief Called by HAL on initialization of the UART
*/
extern "C" void HAL_UART_MspInit(UART_HandleTypeDef* uartHandle)
{
	GPIO_InitTypeDef GPIO_InitStruct;
	if(uartHandle->Instance==USART2)
	{
		__HAL_RCC_GPIOA_CLK_ENABLE();

		GPIO_InitStruct.Pin = USART_TX_PIN | USART_RX_PIN;
		GPIO_InitStruct.Mode = GPIO_MODE_AF_PP;
		GPIO_InitStruct.Pull = GPIO_NOPULL;
		GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_LOW;
		GPIO_InitStruct.Alternate = GPIO_AF7_USART2;
		HAL_GPIO_Init(GPIOA, &GPIO_InitStruct);

		HAL_NVIC_SetPriority(USART2_IRQn, 0, 0);
		HAL_NVIC_EnableIRQ(USART2_IRQn);
	}
}


/*!	\brief The UART IRQ handler
*/
extern "C" void USART2_IRQHandler(void)
{
	HAL_UART_IRQHandler(&huart2);
}


